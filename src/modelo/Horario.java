package modelo;

/**
 *
 * @author Usuario
 */
public class Horario {
    private int codHor;
    private String entraHor;
    private String saliHor;
    private String fechHor;

    public int getCodHor() {
        return codHor;
    }

    public void setCodHor(int codHor) {
        this.codHor = codHor;
    }

    public String getEntraHor() {
        return entraHor;
    }

    public void setEntraHor(String entraHor) {
        this.entraHor = entraHor;
    }

    public String getSaliHor() {
        return saliHor;
    }

    public void setSaliHor(String saliHor) {
        this.saliHor = saliHor;
    }

    public String getFechHor() {
        return fechHor;
    }

    public void setFechHor(String fechHor) {
        this.fechHor = fechHor;
    }
    
    
}
