public class Ubigeo {
    private String numUbi;
    private String depUbi;
    private String proUbi;
    private String distUbi;

    public String getNumUbi() {
        return numUbi;
    }

    public void setNumUbi(String numUbi) {
        this.numUbi = numUbi;
    }

    public String getDepUbi() {
        return depUbi;
    }

    public void setDepUbi(String depUbi) {
        this.depUbi = depUbi;
    }

    public String getProUbi() {
        return proUbi;
    }

    public void setProUbi(String proUbi) {
        this.proUbi = proUbi;
    }

    public String getDistUbi() {
        return distUbi;
    }

    public void setDistUbi(String distUbi) {
        this.distUbi = distUbi;
    }
    
}
